package lesson_28.contacts;

import java.util.Map;

public class Contacts {
    private Map<String,String> contacts;

    public Contacts() {
        this.contacts = contacts;
    }

    public Map<String, String> getContacts() {
        return contacts;
    }


    @Override
    public String toString() {
        return "Contacts{" +
                "contacts=" + contacts +
                '}';
    }
}
